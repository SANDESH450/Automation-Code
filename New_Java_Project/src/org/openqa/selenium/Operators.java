package org.openqa.selenium;

public class Operators {
	
	public static void main(String[] args) {
		
		// Arithmetic Operators (+, -, *, /, %, ++, and --)
		
		int a = 10;
		int b = 5;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);

		System.out.println(a/b);

		System.out.println(a%b);

		// ++ increment Operator
		
		System.out.println(a--);
		System.out.println(b++);
		
		// -- Decrement Operator 
		System.out.println(a--);
		System.out.println(b--);
		

		
		
	}

}
