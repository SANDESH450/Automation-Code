package Control_Statments;

public class Switch {
	
	public static void main(String[] args) {
		
		int day = 2;
		
		switch(day) {
			 
			case 1:
				System.out.println("Tuesday");
				break;
				
			case 2:
				System.out.println("Monday");
				break;
				
				default:
					System.out.println("case are not match");
				
		}
	}

}
